| Parameter | Type | Required | Description |
| - | - | - | - |
| `slug` | `string` | `required` | Needs to be unique slug/folder name for the new subpage |
| `template` | `string` | <code></code> | The name of the page template. If not passed, the default template will be used |
| `content` | `array` | <code></code> | Additional content fields. Must be defined as key/value array. Pass `title` as key, otherwise the slug will be used as page title. |
